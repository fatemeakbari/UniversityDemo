package com.proliferay.myportlet.model;

public class DumpStudent {
    
    int id;
    String name;
    String city;

    public DumpStudent() {
    }

    public int getId() {
        return id;
    }

    public void setId(int sid) {
        this.id = sid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
